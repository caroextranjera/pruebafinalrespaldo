function register_enqueue_style(){
	$theme_data = wp_get_theme();

wp_register_style('fontawesome', 'https://use.fontawesome.com/releases/v5.1.0/css/all.css');

wp_register_style('mitemacss', 'https://stackpath.bootstrapcdn.com/bootstrap/4.1.3/css/bootstrap.min.css');
wp_register_style ('style-css', get_theme_file_uri('css/style.css'), null,  null, 'screen');

wp_enqueue_style('fontawesome');
wp_enqueue_style('mitemacss');
wp_enqueue_style('style-css');


}

add_action( 'wp_enqueue_scripts', 'register_enqueue_style' );



function register_enqueue_scripts(){
	$theme_data = wp_get_theme();

	wp_deregister_script( 'jquery' );
	wp_deregister_script( 'jquery-migrate' );

	wp_register_script('jquery', 'https://ajax.googleapis.com/ajax/libs/jquery/3.3.1/jquery.min.js');

	wp_register_script('min', 'https://stackpath.bootstrapcdn.com/bootstrap/4.1.3/js/bootstrap.min.js');

	wp_register_script('popper', 'https://cdnjs.cloudflare.com/ajax/libs/popper.js/1.14.3/umd/popper.min.js');

    wp_register_script('script', get_theme_file_uri('js/script.js'));

	wp_enqueue_script('jquery');
	wp_enqueue_script('min');
	wp_enqueue_script('popper');
	wp_enqueue_script('script');

}

add_action( 'wp_enqueue_scripts', 'register_enqueue_scripts' );

remove_action ('shutdown', 'wp_ob_end_flush_all', 1);


